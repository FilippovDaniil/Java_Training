package m07_multidimensional_arrays.practice;

/**
 * Задача 06 — Модуль 07: Зубчатый массив (треугольник Паскаля)
 *
 * ЗАДАНИЕ:
 *   Создайте зубчатый (jagged) массив, где строка i содержит i+1
 *   элементов, и заполните его так, чтобы получился треугольник
 *   Паскаля для 5 строк. Затем выведите его.
 *
 * ПРАВИЛО ТРЕУГОЛЬНИКА ПАСКАЛЯ:
 *   - крайние элементы строки равны 1;
 *   - внутренний элемент = сумма двух элементов сверху:
 *     t[i][j] = t[i-1][j-1] + t[i-1][j]
 *
 * ОЖИДАЕМЫЙ ВЫВОД:
 *   1
 *   1 1
 *   1 2 1
 *   1 3 3 1
 *   1 4 6 4 1
 *
 * ПОДСКАЗКА:
 *   int[][] t = new int[5][];  затем t[i] = new int[i + 1];
 */
public class Task06 {
    public static void main(String[] args) {
        int rows = 5;

        // 1. Создаём зубчатый массив: каждая строка имеет разную длину
        int[][] triangle = new int[rows][];

        // 2. Заполняем массив по правилу треугольника Паскаля
        for (int i = 0; i < rows; i++) {
            // строка i содержит i+1 элементов
            triangle[i] = new int[i + 1];

            // первый и последний элемент каждой строки = 1
            triangle[i][0] = 1;
            triangle[i][i] = 1;

            // заполняем внутренние элементы (если строка длиннее 2)
            for (int j = 1; j < i; j++) {
                // каждый элемент = сумма двух элементов из предыдущей строки
                triangle[i][j] = triangle[i - 1][j - 1] + triangle[i - 1][j];
            }
        }

        // 3. Выводим треугольник Паскаля
        for (int i = 0; i < triangle.length; i++) {
            for (int j = 0; j < triangle[i].length; j++) {
                System.out.print(triangle[i][j] + " ");
            }
            System.out.println();
        }
    }
}
