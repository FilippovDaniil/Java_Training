package m45_sql_dml.practice;

/**
 * Задача 06 — Модуль 45: DELETE (безопасно)
 *
 * ЗАДАНИЕ (SQL) для таблицы employees:
 *   1) сначала напишите SELECT, показывающий сотрудников с зарплатой
 *      ниже 30000 (проверка перед удалением);
 *   2) удалите этих сотрудников (DELETE с тем же WHERE);
 *   3) удалите сотрудников конкретного отдела (например, 'Временный');
 *   4) в комментарии поясните, чем DELETE без WHERE отличается от
 *      TRUNCATE TABLE и DROP TABLE.
 *
 * ВНИМАНИЕ:
 *   DELETE без WHERE удалит ВСЕ строки. Всегда проверяйте условие
 *   через SELECT заранее.
 *
 * ПОДСКАЗКА:
 *   SELECT * FROM employees WHERE salary < 30000;   -- проверка
 *   DELETE FROM employees WHERE salary < 30000;      -- удаление
 */
public class Task06 {
    public static void main(String[] args) {
        String sql = """
            -- ============================================
            -- ПОДГОТОВКА ТАБЛИЦЫ И ДАННЫХ
            -- ============================================
            CREATE TABLE IF NOT EXISTS employees (
                id         INT PRIMARY KEY AUTO_INCREMENT,
                name       VARCHAR(100) NOT NULL,
                department VARCHAR(50),
                salary     DECIMAL(10,2),
                hired      DATE
            );
            
            INSERT INTO employees (name, department, salary, hired) VALUES
                ('Иван Петров', 'IT', 80000.00, '2020-03-15'),
                ('Мария Смирнова', 'HR', 65000.00, '2019-07-22'),
                ('Петр Сидоров', 'IT', 95000.00, '2018-11-01'),
                ('Анна Кузнецова', 'Finance', 72000.00, '2021-02-10'),
                ('Сергей Иванов', 'Marketing', 68000.00, '2020-09-14'),
                ('Елена Петрова', 'Временный', 25000.00, '2024-01-15'),
                ('Алексей Васильев', 'Sales', 70000.00, '2021-06-01'),
                ('Ольга Павлова', 'IT', 90000.00, '2018-03-20'),
                ('Дмитрий Новиков', 'Временный', 28000.00, '2024-02-01'),
                ('Наталья Соколова', 'Finance', 82000.00, '2019-11-10'),
                ('Андрей Морозов', 'Временный', 22000.00, '2024-03-15'),
                ('Александр Волков', 'Marketing', 85000.00, '2020-11-12');
            
            -- ============================================
            -- 1) ПРОВЕРКА SELECT: сотрудники с зарплатой < 30000
            -- ============================================
            SELECT '=== 1) Проверка: сотрудники с зарплатой < 30000 ===' AS info;
            SELECT * FROM employees WHERE salary < 30000;
            
            -- ============================================
            -- 2) УДАЛЕНИЕ СОТРУДНИКОВ С ЗАРПЛАТОЙ < 30000
            -- ============================================
            SELECT '=== 2) Удаление сотрудников с зарплатой < 30000 ===' AS info;
            
            -- Проверка перед удалением (количество)
            SELECT 'Количество сотрудников с зарплатой < 30000: ' || COUNT(*) AS info
            FROM employees WHERE salary < 30000;
            
            -- Удаление
            DELETE FROM employees WHERE salary < 30000;
            
            -- Проверка после удаления
            SELECT 'Количество сотрудников с зарплатой < 30000: ' || COUNT(*) AS info
            FROM employees WHERE salary < 30000;
            
            -- ============================================
            -- 3) УДАЛЕНИЕ СОТРУДНИКОВ КОНКРЕТНОГО ОТДЕЛА
            -- ============================================
            SELECT '=== 3) Удаление сотрудников отдела "Временный" ===' AS info;
            
            -- Проверка перед удалением
            SELECT 'Количество сотрудников в "Временный": ' || COUNT(*) AS info
            FROM employees WHERE department = 'Временный';
            
            -- Удаление
            DELETE FROM employees WHERE department = 'Временный';
            
            -- Проверка после удаления
            SELECT 'Количество сотрудников в "Временный": ' || COUNT(*) AS info
            FROM employees WHERE department = 'Временный';
            
            -- ============================================
            -- ФИНАЛЬНАЯ ПРОВЕРКА
            -- ============================================
            SELECT '=== ИТОГОВЫЙ СПИСОК СОТРУДНИКОВ ===' AS info;
            SELECT * FROM employees ORDER BY id;
            
            -- ============================================
            -- 4) ПОЯСНЕНИЕ: DELETE без WHERE, TRUNCATE, DROP
            -- ============================================
            /*
            ВАЖНО: Эти операции не выполняются в данном скрипте!
            
            DELETE без WHERE:
            - Удаляет ВСЕ строки из таблицы
            - Можно откатить (ROLLBACK) если в транзакции
            - Срабатывают триггеры (ON DELETE)
            - Медленнее, чем TRUNCATE, т.к. логирует каждую строку
            - Можно использовать WHERE для выборочного удаления
            - Пример: DELETE FROM employees; -- удалит все строки
            
            TRUNCATE TABLE:
            - Удаляет ВСЕ строки из таблицы (нельзя с WHERE)
            - Нельзя откатить (в некоторых СУБД)
            - Не срабатывают триггеры
            - Быстрее, чем DELETE
            - Сбрасывает счетчик AUTO_INCREMENT
            - Пример: TRUNCATE TABLE employees;
            
            DROP TABLE:
            - Удаляет таблицу ЦЕЛИКОМ (все данные + структура)
            - Нельзя откатить (в большинстве СУБД)
            - Таблица перестает существовать
            - Нужно создавать таблицу заново
            - Пример: DROP TABLE employees;
            */
            """;
        System.out.println(sql);
    }
}
