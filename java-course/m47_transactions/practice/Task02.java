package m47_transactions.practice;

/**
 * Задача 02 — Модуль 47: ROLLBACK
 *
 * ЗАДАНИЕ (SQL) для таблицы accounts (см. Task01):
 *   1) начните транзакцию;
 *   2) сделайте два UPDATE (изменив балансы);
 *   3) вместо COMMIT выполните ROLLBACK;
 *   4) проверьте SELECT-ом, что балансы НЕ изменились — транзакция
 *      отменена.
 *   В комментарии опишите, в каких ситуациях нужен ROLLBACK
 *   (например, при ошибке в середине операции).
 *
 * ПОДСКАЗКА:
 *   BEGIN; UPDATE ...; UPDATE ...; ROLLBACK;
 *   После ROLLBACK данные останутся как до BEGIN.
 */
public class Task02 {
    public static void main(String[] args) {
        String sql = """
            -- ============================================
            -- ПОДГОТОВКА ТАБЛИЦЫ accounts
            -- ============================================
            CREATE TABLE IF NOT EXISTS accounts (
                id      VARCHAR(10) PRIMARY KEY,
                balance DECIMAL(12, 2) CHECK (balance >= 0)
            );
            
            -- Очищаем и наполняем данными
            DELETE FROM accounts;
            INSERT INTO accounts (id, balance) VALUES
                ('A', 1000.00),
                ('B', 500.00),
                ('C', 2000.00);
            
            -- Проверка начальных балансов
            SELECT '=== НАЧАЛЬНЫЕ БАЛАНСЫ ===' AS info;
            SELECT * FROM accounts ORDER BY id;
            
            -- ============================================
            -- ТРАНЗАКЦИЯ С ROLLBACK
            -- ============================================
            
            -- Начало транзакции
            BEGIN;
            
            -- Изменяем балансы
            UPDATE accounts SET balance = balance - 300 WHERE id = 'A';
            UPDATE accounts SET balance = balance + 300 WHERE id = 'B';
            
            -- Проверка состояния ВНУТРИ транзакции
            SELECT '=== СОСТОЯНИЕ ВНУТРИ ТРАНЗАКЦИИ (ДО ROLLBACK) ===' AS info;
            SELECT * FROM accounts ORDER BY id;
            
            -- Откат транзакции
            ROLLBACK;
            
            -- ============================================
            -- ПРОВЕРКА ПОСЛЕ ROLLBACK
            -- ============================================
            SELECT '=== СОСТОЯНИЕ ПОСЛЕ ROLLBACK ===' AS info;
            SELECT * FROM accounts ORDER BY id;
            
            -- ============================================
            -- ДЕМОНСТРАЦИЯ ROLLBACK ПРИ ОШИБКЕ
            -- ============================================
            
            -- Транзакция с ошибкой
            BEGIN;
            
            UPDATE accounts SET balance = balance - 100 WHERE id = 'A';
            -- Следующая операция вызовет ошибку (CHECK constraint)
            UPDATE accounts SET balance = balance - 1000 WHERE id = 'B';
            
            -- Если ошибка - откатываем транзакцию
            ROLLBACK;
            
            SELECT '=== ПОСЛЕ ОШИБОЧНОЙ ТРАНЗАКЦИИ (ROLLBACK) ===' AS info;
            SELECT * FROM accounts ORDER BY id;
            
            -- ============================================
            -- ПОЯСНЕНИЕ
            -- ============================================
            /*
            СИТУАЦИИ, КОГДА НУЖЕН ROLLBACK:
            
            1. ОШИБКА В СЕРЕДИНЕ ОПЕРАЦИИ
               - Некорректные данные
               - Нарушение ограничений (CHECK, FOREIGN KEY)
               - Ошибка при расчетах
            
            2. НЕДОСТАТОЧНО СРЕДСТВ
               - При переводе средств баланс становится отрицательным
               - Нельзя списать больше, чем есть
            
            3. ПРОБЛЕМЫ С СВЯЗАННЫМИ ТАБЛИЦАМИ
               - Нет соответствующей записи в связанной таблице
               - Нарушение внешнего ключа
            
            4. ЛОГИЧЕСКИЕ ОШИБКИ В БИЗНЕС-ЛОГИКЕ
               - Перевод на несуществующий счет
               - Некорректная сумма (отрицательная или нулевая)
            
            5. ОШИБКИ СЕТИ ИЛИ СИСТЕМЫ
               - Потеря соединения с БД
               - Сбой сервера
            
            6. ТЕСТИРОВАНИЕ И ОТЛАДКА
               - Проверка изменений без фиксации
               - Эксперименты с данными
            
            7. КОНФЛИКТЫ ПАРАЛЛЕЛЬНЫХ ТРАНЗАКЦИЙ
               - Deadlock (взаимная блокировка)
               - Конфликт изменений
            */
            """;
        System.out.println(sql);
    }
}
