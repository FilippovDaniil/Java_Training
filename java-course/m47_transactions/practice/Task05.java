package m47_transactions.practice;

/**
 * Задача 05 — Модуль 47: Уровни изоляции
 *
 * ЗАДАНИЕ (SQL + анализ):
 *   1) посмотрите текущий уровень изоляции вашей СУБД (в H2/PostgreSQL/
 *      MySQL команда отличается — найдите её);
 *   2) установите уровень READ COMMITTED для транзакции
 *      (SET TRANSACTION ISOLATION LEVEL READ COMMITTED);
 *   3) в комментарии заполните таблицу: какой уровень какие аномалии
 *      (грязное/неповторяющееся/фантомное чтение) допускает.
 *
 * АНАЛИЗ (заполните в комментарии):
 *   READ UNCOMMITTED → допускает: ...
 *   READ COMMITTED   → допускает: ...
 *   REPEATABLE READ  → допускает: ...
 *   SERIALIZABLE     → допускает: ...
 *
 * ПОДСКАЗКА:
 *   SET TRANSACTION ISOLATION LEVEL <уровень>;
 *   Сверьтесь с таблицей аномалий в theory.md.
 */
public class Task05 {
    public static void main(String[] args) {
        String sql = """
            -- ============================================
            -- ПОЛНЫЙ АНАЛИЗ УРОВНЕЙ ИЗОЛЯЦИИ
            -- ============================================
            
            -- ============================================
            -- 1. ОПРЕДЕЛЕНИЯ АНОМАЛИЙ
            -- ============================================
            
            /*
            ГРЯЗНОЕ ЧТЕНИЕ (Dirty Read):
            - Чтение данных, которые были изменены другой транзакцией,
              но ещё не зафиксированы (COMMIT не сделан)
            - Опасно: если транзакция откатится (ROLLBACK), то прочитанные
              данные никогда не существовали в БД
            
            НЕПОВТОРЯЮЩЕЕСЯ ЧТЕНИЕ (Non-Repeatable Read):
            - Два чтения одного и того же поля в одной транзакции
              дают разные результаты
            - Причина: другая транзакция изменила данные между чтениями
            
            ФАНТОМНОЕ ЧТЕНИЕ (Phantom Read):
            - При повторном запросе по тому же условию появляются
              новые строки (или исчезают старые)
            - Причина: другая транзакция вставила или удалила строки
            */
            
            -- ============================================
            -- 2. ДЕТАЛЬНАЯ ТАБЛИЦА АНОМАЛИЙ
            -- ============================================
            
            /*
            ┌─────────────────────────────────────────────────────────────────────┐
            │                    УРОВНИ ИЗОЛЯЦИИ И АНОМАЛИИ                      │
            ├───────────────────────┬─────────────┬─────────────┬─────────────┬──────────────┤
            │    УРОВЕНЬ            │  ГРЯЗНОЕ    │НЕПОВТОРЯЮ-  │  ФАНТОМНОЕ  │  ПРОИЗВОДИ-  │
            │                       │  ЧТЕНИЕ     │ЩЕЕСЯ ЧТЕНИЕ │  ЧТЕНИЕ     │  ТЕЛЬНОСТЬ   │
            ├───────────────────────┼─────────────┼─────────────┼─────────────┼──────────────┤
            │READ UNCOMMITTED       │     ✅      │     ✅      │     ✅      │   Самая      │
            │(грязное чтение)       │             │             │             │   высокая    │
            ├───────────────────────┼─────────────┼─────────────┼─────────────┼──────────────┤
            │READ COMMITTED         │     ❌      │     ✅      │     ✅      │   Средняя    │
            │(стандарт)             │             │             │             │              │
            ├───────────────────────┼─────────────┼─────────────┼─────────────┼──────────────┤
            │REPEATABLE READ        │     ❌      │     ❌      │     ✅      │   Средняя    │
            │(MySQL по умолчанию)   │             │             │             │              │
            ├───────────────────────┼─────────────┼─────────────┼─────────────┼──────────────┤
            │SERIALIZABLE           │     ❌      │     ❌      │     ❌      │   Самая      │
            │(полная изоляция)      │             │             │             │   низкая     │
            └───────────────────────┴─────────────┴─────────────┴─────────────┴──────────────┘
            
            ✅ = допускает аномалию
            ❌ = НЕ допускает аномалию (защищает от неё)
            */
            
            -- ============================================
            -- 3. УСТАНОВКА РАЗНЫХ УРОВНЕЙ ИЗОЛЯЦИИ
            -- ============================================
            
            -- READ UNCOMMITTED (H2)
            SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;
            
            -- READ COMMITTED (PostgreSQL/Standard)
            SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
            
            -- REPEATABLE READ (MySQL default)
            SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
            
            -- SERIALIZABLE
            SET TRANSACTION ISOLATION LEVEL SERIALIZABLE;
            
            -- ============================================
            -- 4. ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ
            -- ============================================
            
            /*
            ГДЕ КАКОЙ УРОВЕНЬ ИСПОЛЬЗОВАТЬ:
            
            1. READ UNCOMMITTED:
               - Для отчетов, где допустима небольшая неточность
               - Для очень быстрых чтений
               - Для мониторинга
               - НЕ использовать для финансовых операций!
            
            2. READ COMMITTED:
               - Стандартный уровень для большинства приложений
               - Хороший баланс между производительностью и согласованностью
               - Рекомендуется для большинства веб-приложений
            
            3. REPEATABLE READ:
               - Когда нужно гарантировать одинаковый результат при
                 повторном чтении в рамках транзакции
               - Для аналитических отчетов
               - Для сложных запросов с подзапросами
            
            4. SERIALIZABLE:
               - Для финансовых операций
               - Для работы с деньгами
               - Когда абсолютная согласованность критична
               - Цена: низкая производительность
            */
            
            -- ============================================
            -- 5. ДЕМОНСТРАЦИОННЫЙ ПРИМЕР
            -- ============================================
            
            -- Подготовка данных
            CREATE TABLE IF NOT EXISTS accounts_test (
                id      VARCHAR(10) PRIMARY KEY,
                balance DECIMAL(12, 2)
            );
            
            INSERT INTO accounts_test (id, balance) VALUES
                ('A', 1000.00),
                ('B', 500.00);
            
            -- Пример: READ COMMITTED (без грязного чтения)
            BEGIN;
            SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
            UPDATE accounts_test SET balance = 800 WHERE id = 'A';
            -- Ещё не COMMIT
            
            -- В другой транзакции (READ COMMITTED) НЕ увидит изменений
            -- SELECT * FROM accounts_test WHERE id = 'A'; -- вернет 1000
            
            COMMIT;
            
            -- Проверка
            SELECT * FROM accounts_test ORDER BY id;
            """;
        System.out.println(sql);
    }
}
