package m47_transactions.practice;

/**
 * Задача 06 — Модуль 47: Понимание аномалий
 *
 * ЗАДАНИЕ (анализ + SQL-комментарии):
 *   Для каждой аномалии опишите СЦЕНАРИЙ из двух транзакций (T1 и T2),
 *   при котором она возникает, и укажите, какой минимальный уровень
 *   изоляции её предотвращает:
 *     1) Грязное чтение (dirty read);
 *     2) Неповторяющееся чтение (non-repeatable read);
 *     3) Фантомное чтение (phantom read).
 *   Запишите сценарии как последовательность шагов в комментарии,
 *   например:
 *     T1: BEGIN; читает X=100
 *     T2: BEGIN; UPDATE X=200 (без COMMIT)
 *     T1: читает X=200  ← аномалия
 *
 * ЦЕЛЬ:
 *   Прочувствовать, ПОЧЕМУ нужны уровни изоляции, на конкретных
 *   сценариях параллельных транзакций.
 *
 * ПОДСКАЗКА:
 *   Опирайтесь на таблицу аномалий из theory.md.
 */
public class Task06 {
    public static void main(String[] args) {
        String sql = """
            -- ============================================
            -- СЦЕНАРИИ АНОМАЛИЙ ТРАНЗАКЦИЙ
            -- ============================================
            
            -- Подготовка таблицы для демонстрации
            CREATE TABLE IF NOT EXISTS accounts (
                id      VARCHAR(10) PRIMARY KEY,
                balance DECIMAL(12, 2)
            );
            
            INSERT INTO accounts (id, balance) VALUES
                ('A', 1000.00),
                ('B', 500.00);
            
            -- ============================================
            -- 1) ГРЯЗНОЕ ЧТЕНИЕ (DIRTY READ)
            -- ============================================
            
            /*
            ОПИСАНИЕ:
            Транзакция T1 читает данные, которые были изменены транзакцией T2,
            но ещё не зафиксированы (COMMIT не сделан).
            
            СЦЕНАРИЙ:
            
            Время | T1 (READ UNCOMMITTED)         | T2 (READ UNCOMMITTED)
            ------|--------------------------------|-------------------------------
            1     | BEGIN;                         |
            2     |                                | BEGIN;
            3     |                                | UPDATE accounts 
                  |                                | SET balance = balance - 300
                  |                                | WHERE id = 'A';
            4     | SELECT balance FROM accounts   |
                  | WHERE id = 'A';                |
                  | -- Видит 700 (ещё не COMMIT!)  |
            5     |                                | ROLLBACK;  -- откат!
            6     | -- T1 имеет данные (700),      |
                  | -- которых никогда не было     |
            7     | COMMIT;                        |
            
            АНОМАЛИЯ:
            T1 видит значение 700, которое никогда не существовало 
            (T2 откатила транзакцию).
            
            МИНИМАЛЬНЫЙ УРОВЕНЬ ИЗОЛЯЦИИ:
            READ COMMITTED - запрещает чтение незафиксированных данных.
            */
            
            -- ============================================
            -- 2) НЕПОВТОРЯЮЩЕЕСЯ ЧТЕНИЕ (NON-REPEATABLE READ)
            -- ============================================
            
            /*
            ОПИСАНИЕ:
            В рамках одной транзакции T1 два одинаковых запроса дают разные 
            результаты, потому что между ними транзакция T2 изменила данные.
            
            СЦЕНАРИЙ:
            
            Время | T1 (READ COMMITTED)            | T2 (READ COMMITTED)
            ------|--------------------------------|-------------------------------
            1     | BEGIN;                         |
            2     | SELECT balance FROM accounts   |
                  | WHERE id = 'A';                |
                  | -- Результат: 1000             |
            3     |                                | BEGIN;
            4     |                                | UPDATE accounts 
                  |                                | SET balance = 800
                  |                                | WHERE id = 'A';
            5     |                                | COMMIT;
            6     | SELECT balance FROM accounts   |
                  | WHERE id = 'A';                |
                  | -- Результат: 800              |
            7     | -- Разные результаты за два    |
                  | -- чтения в одной транзакции!  |
            8     | COMMIT;                        |
            
            АНОМАЛИЯ:
            T1 читает разные значения (1000 и 800) в рамках одной транзакции.
            
            МИНИМАЛЬНЫЙ УРОВЕНЬ ИЗОЛЯЦИИ:
            REPEATABLE READ - гарантирует одинаковый результат при повторных чтениях.
            */
            
            -- ============================================
            -- 3) ФАНТОМНОЕ ЧТЕНИЕ (PHANTOM READ)
            -- ============================================
            
            /*
            ОПИСАНИЕ:
            В рамках одной транзакции T1 два одинаковых запроса с условием
            дают разное количество строк, потому что между ними транзакция T2
            добавила или удалила строки, удовлетворяющие условию.
            
            СЦЕНАРИЙ:
            
            Время | T1 (REPEATABLE READ)           | T2 (REPEATABLE READ)
            ------|--------------------------------|-------------------------------
            1     | BEGIN;                         |
            2     | SELECT COUNT(*) FROM accounts  |
                  | WHERE balance > 500;           |
                  | -- Результат: 1 (счет A)       |
            3     |                                | BEGIN;
            4     |                                | INSERT INTO accounts 
                  |                                | (id, balance) 
                  |                                | VALUES ('C', 600);
            5     |                                | COMMIT;
            6     | SELECT COUNT(*) FROM accounts  |
                  | WHERE balance > 500;           |
                  | -- Результат: 2 (счета A и C)  |
            7     | -- Разное количество строк!    |
            8     | COMMIT;                        |
            
            АНОМАЛИЯ:
            T1 видит разное количество строк (1 и 2) при повторном запросе.
            
            МИНИМАЛЬНЫЙ УРОВЕНЬ ИЗОЛЯЦИИ:
            SERIALIZABLE - предотвращает добавление/удаление строк,
            удовлетворяющих условию запроса.
            */
            
            -- ============================================
            -- ДОПОЛНИТЕЛЬНЫЙ СЦЕНАРИЙ: СРАВНЕНИЕ АНОМАЛИЙ
            -- ============================================
            
            /*
            СВОДКА:
            
            Аномалия                 | Минимальный уровень изоляции
            -------------------------|-------------------------------
            Грязное чтение           | READ COMMITTED
            Неповторяющееся чтение   | REPEATABLE READ
            Фантомное чтение         | SERIALIZABLE
            
            ГДЕ КАКОЙ УРОВЕНЬ ИСПОЛЬЗОВАТЬ:
            
            1. READ UNCOMMITTED:
               - Не используется в production
               - Только для очень грубых отчетов
               - Допускает ВСЕ аномалии
            
            2. READ COMMITTED:
               - Стандарт для большинства приложений
               - Допускает: неповторяющееся и фантомное чтение
               - Защищает от грязного чтения
            
            3. REPEATABLE READ:
               - Когда нужна согласованность данных
               - Допускает: фантомное чтение
               - Защищает от грязного и неповторяющегося чтения
            
            4. SERIALIZABLE:
               - Для финансовых операций
               - Не допускает НИКАКИХ аномалий
               - Самая низкая производительность
            */
            
            -- ============================================
            -- ПРОВЕРКА ПОНИМАНИЯ
            -- ============================================
            
            SELECT '=== ПРОВЕРКА ПОНИМАНИЯ ===' AS info;
            SELECT 
                'Грязное чтение' AS anomaly,
                'READ COMMITTED' AS min_level,
                'Чтение незафиксированных данных' AS description
            UNION ALL
            SELECT 
                'Неповторяющееся чтение',
                'REPEATABLE READ',
                'Разные результаты при повторном чтении'
            UNION ALL
            SELECT 
                'Фантомное чтение',
                'SERIALIZABLE',
                'Появление/исчезновение строк при повторном запросе';
            """;
        System.out.println(sql);
    }
}
