@Service
class JwtService05 {
    private final SecretKey key;
    JwtService05(@Value("${jwt.secret:01234567890123456789012345678901}") String secret) {
        this.key = Keys.hmacShaKeyFor(secret.getBytes(StandardCharsets.UTF_8));
    }
    String generateToken(String username, List<String> roles) {
        return Jwts.builder().subject(username).claim("roles", roles)
                .issuedAt(new Date()).expiration(new Date(System.currentTimeMillis() + 3_600_000))
                .signWith(key).compact();
    }
    Claims claims(String token) {
        return Jwts.parser().verifyWith(key).build().parseSignedClaims(token).getPayload();
    }
}
